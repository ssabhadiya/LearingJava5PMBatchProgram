public class LabExIfElse15 {
    public static void main(String[] args) {

        // if age > 10 then -> vote allowed

        // Condition - Switch, If

        int age = 13;
        if (age > 18) {
            System.out.println("Allowed");
        } else {
            System.out.println("Not allowed");
        }

        boolean b = true;

        if (b) {
            System.out.println("B is true");
        } else {
            System.out.println("B is false");
        }

        boolean c = !true;

        if (c) {
            System.out.println("B is true");
        } else {
            System.out.println("B is false");
        }

        if (2 + 2 >= 4) {
            System.out.println("True with inside condition");
        }

    }
}
